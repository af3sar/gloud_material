let a = 5; //binary; 0101
let b = 3; //binary: 0011

//AND 
console.log("Bitwise AND (a & b):", a & b);

//OR 
console.log("Bitwise OR (a | b):", a | b);

//XOR
console.log("Bitwise XOR(a^b):", a ^ b);

//NOT
console.log("Bitwise NOT (~a):", ~a);

//Left Shift
console.log("Left Shift (a << 1):", a << 1);

//Right Shift
console.log("Left shift (a >> 1):", a >> 1);